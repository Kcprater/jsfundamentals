// ASSIGNMENT OPERATOR
let r = 5;

// ADDITION ASSIGNMENT
r += 1; // r = r + 1

// SUBTRACTION
r -=1; // r= r - 1

// MULTIPLICATION ASSIGNMENT
r *= 1; // r = r * 1

// DIVISION ASSIGNMENT
r /= 1; // r = r / 1

// REMAINDER (or modules) ASSIGNMENT
r %= 1 // r = r % 1

// EXPONENTIAL ASSIGNMENT
r **= 1; // r = r ** 1